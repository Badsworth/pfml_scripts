/**
 * @see https://github.com/seek-oss/playroom
 */
const CopyPlugin = require("copy-webpack-plugin");
const exampleCode = require("./playroom/exampleCode");
const nextConfig = require("./next.config");

/**
 * Playroom doesn't handle compilation for us, so we need to
 * configure Webpack for both React and Sass compilation...
 */
const webpack = {
  resolve: {
    extensions: [".js", ".scss", ".css"],
  },
  module: {
    rules: [
      {
        test: /\.js$/,
        exclude: /node_modules|builds/,
        use: {
          loader: "babel-loader",
          options: {
            presets: ["next/babel"],
          },
        },
      },
      {
        test: /\.s[ac]ss$/i,
        exclude: /node_modules/,
        use: [
          "style-loader",
          "css-loader",
          {
            loader: "sass-loader",
            options: {
              sassOptions: nextConfig.sassOptions,
            },
          },
        ],
      },
      {
        // Support Sass imports of these file types:
        test: /\.(png|jpe?g|gif|svg|eot|ttf|woff|woff2)$/i,
        use: ["file-loader"],
      },
    ],
  },
  plugins: [
    // Our static assets need copied over into the build directory
    // so that Playroom can load them properly
    new CopyPlugin({
      patterns: [{ from: "public", to: "" }],
    }),
  ],
};

module.exports = {
  components: "./playroom/autogenerated/components.js",
  outputPath: "./builds/playroom",
  frameComponent: "./playroom/preview.js",
  title: "PFML Playroom",
  widths: [320, 800],
  port: 9000,
  openBrowser: true,
  exampleCode,
  webpackConfig: () => webpack,
};
