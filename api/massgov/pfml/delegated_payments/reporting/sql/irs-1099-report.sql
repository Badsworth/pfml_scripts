-- 1099s Report
SELECT IRS_1099.LAST_NAME, IRS_1099.FIRST_NAME, E.FINEOS_CUSTOMER_NUMBER,
	IRS_1099.ADDRESS_LINE_1, IRS_1099.ADDRESS_LINE_2, IRS_1099.CITY, IRS_1099.STATE, IRS_1099.ZIP,
	IRS_1099.GROSS_PAYMENTS, IRS_1099.STATE_TAX_WITHHOLDINGS, IRS_1099.FEDERAL_TAX_WITHHOLDINGS, IRS_1099.OVERPAYMENT_REPAYMENTS,
    IRS_1099.CORRECTION_IND
FROM PFML_1099 IRS_1099
LEFT OUTER JOIN EMPLOYEE E ON IRS_1099.EMPLOYEE_ID = E.EMPLOYEE_ID
WHERE IRS_1099.PFML_1099_BATCH_ID = (SELECT PFML_1099_BATCH_ID FROM PFML_1099_BATCH BATCH ORDER BY BATCH.CREATED_AT DESC LIMIT 1)